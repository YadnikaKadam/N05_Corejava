//Program to demonstrate on Relational Operator

package org.tnsif.operators;

public class RelationalOperator {

	public static void main(String[] args) {
		System.out.println(5==7);
		System.out.println(5!=7);
		System.out.println(5<7);
		System.out.println(5>7);
		System.out.println(5<=7);
		System.out.println(5>=7);
		
	}

}
